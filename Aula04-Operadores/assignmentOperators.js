// Operador de atribuição (=)
// Além do operador de atribuição simples, existem também operadores de atribuição combinados, como +=, -=, *=, /=, que realizam uma operação aritmética antes de atribuir o resultado. 

let salarioAbril = 5000;

// salarioAbril = salarioAbril + 3000;
salarioAbril += 3000;
console.log("O salário líquido de Abril é: ", salarioAbril);

let salarioMaio = 7000;

//salarioMaio = salarioMaio - 2000;
salarioMaio -= 2000;
console.log("O salário líquido de Maio é: ", salarioMaio);

